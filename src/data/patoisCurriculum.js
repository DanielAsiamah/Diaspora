const lesson = ({
  id,
  order,
  title,
  subtitle,
  phrase,
  meaning,
  note,
  choices,
  type = 'star',
  xp = 20,
}) => ({
  id,
  order,
  status: 'published',
  version: 1,
  title,
  subtitle,
  phrase,
  meaning,
  note,
  type,
  xp,
  audioKey: null,
  quiz: {
    prompt: `What does "${phrase}" mean?`,
    answer: meaning,
    choices,
  },
});

const chest = (id, title, xp = 45) => ({
  id,
  title,
  type: 'chest',
  xp,
  claimed: false,
});

